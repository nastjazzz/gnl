#include "libft.h"

char	*ft_strcat(char *str1, const char *str2)
{
	int i;
	int j;

	i = ft_strlen(str1);
	j = 0;
	while (str2[j])
	{
		str1[i + j] = str2[j];
		j++;
	}
	str1[i + j] = '\0';
	return (str1);
}
